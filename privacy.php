This Privacy Policy describes the information we collect from you, what we do with the information, and our information security practices.  If you have any questions about our Privacy Policy, please <a href="http://tamuhvz.com/contact.php">contact us</a><br />
<h3>What information do we collect?</h3>
When registering on our site you will be asked to enter your name and e-mail address. In addition, we track the last time you visited the site. You must login to the site in order to view its content.<br />
The first time you visit the site's Board and each time you choose to post on the site's Board, your IP address will be logged.<br />
Users that registered before March 25, 2010 were asked to enter their University Identification Number (UIN). This number has been completely removed from the database and replaced with a non university-related number (your "user id") on April 4, 2010.<br />
