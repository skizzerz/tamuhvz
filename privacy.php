We use your information in the following ways:
<ul>
<li>We use your name to display to others registered for the game so that you may be identified.</li>
<li>We use your e-mail address to periodically send you important information.</li>
<li>We use the time you last visited the site as a means of determining your level of activity.</li>
<li>We use your IP address to help track down issues with the site.</li>
</ul>
